function exibirTreino(dia) {
    let treino = treino_atual.treino.find(t => t.dia === dia);
    console.log(treino);
    $('.exercicio').remove();

    treino.exercicios.forEach((nome_ex, index) => {
        let exercicio = exercicios.find(ex => ex.nome === nome_ex);

        let reps = `<small><span class="series">4</span>&times;<span class="reps">12</span></small>`;

        if (exercicio) $('#treino').append(
            `<div class="exercicio">
                <div class="resumido">
                    <p class="nome">
                        ${exercicio.nome}
                        <input type="checkbox" class="btn-check" id="btn-check-ex-${index}" autocomplete="off">
                        <label class="btn btn-lg btn-outline-danger btn-ckeckbox-exercicio" for="btn-check-ex-${index}"><i class="bi bi-check-lg"></i></label>
                    </p>

                </div>
                <div class="detalhado">
                    <table>
                        <tr>
                            <td>Musculatura alvo</td>
                            <td>${exercicio.grupo_muscular}</td>
                        </tr>
                        <tr>
                            <td>Equipamento</td>
                            <td>${exercicio.equipamento}</td>
                        </tr>
                    </table>
                </div>
            </div>`
        );
    });
}

$(document).ready(function () {
    $(document).on('click', '.nome input, .nome label', function (e) {
        e.stopPropagation();
    });

    $(document).on('click', '.exercicio', function () {
        let detalhes = $(this).find('.detalhado');

        if (detalhes.is(':hidden'))
            detalhes.slideDown();
        else
            detalhes.slideUp();
        
        $('.exercicio .detalhado').not(detalhes).slideUp();
    });

    $(document).on('click', '.btn-ckeckbox-exercicio', function () {
        let checkbox = $(this).prev('input[type="checkbox"]');
        let exercicio = $(this).closest('.nome');

        if (checkbox.is(':checked'))
            exercicio.removeClass('exercicio-check');
        else
            exercicio.addClass('exercicio-check');
    });

    $('.treino-btn').on('click', function () {
        console.log($(this).data('dia'));
        exibirTreino($(this).data('dia'));

        $('#treino h1').text($(this).text());
        $('#treino').show(0, function() {
            $('#home').slideUp(700, 'easeInOutQuint');
        });
    });
});

window.addEventListener('load', function () {
    history.pushState({ page: 1 }, '', '');

    window.addEventListener('popstate', function () {
        $('#home').slideDown(700, 'easeInOutQuint', function() {
            $('#treino').hide(0);
        });
    });
});